#include "../inc/so_long.h"

int	ft_check(char *str)
{
	int	i;

	i = 0;
	while (str[i] == '\t' || str[i] == '\r'
		|| str[i] == '\v' || str[i] == ' ')
		i++;
	return (i);
}
